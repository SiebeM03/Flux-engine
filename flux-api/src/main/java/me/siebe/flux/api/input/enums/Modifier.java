package me.siebe.flux.api.input.enums;

/**
 * Modifier keys (shift, control, alt, etc.) that can be held during keyboard or mouse events.
 * Used with {@link me.siebe.flux.api.input.keyboard.event.KeyEvent} and {@link me.siebe.flux.api.input.mouse.event.MouseButtonEvent}.
 */
public enum Modifier {
    SHIFT,
    CONTROL,
    ALT,
    SUPER,
    CAPS_LOCK,
    NUM_LOCK
}
